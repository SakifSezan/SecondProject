package secondjavaclass;

public class Practice_Now_Static {

	public static void main(String[] args) {
		Practice_Now_Static.Sakif();
		Practice_Now_Static.Sezan();

	}

	public static void Sakif() {
		int a = 779;
		int b = -50;
		int c = a+b;
		System.out.println(c);
	}
	public static void Sezan() {
		Double x = 10.99;
		Double y = 17.99;
		Double z = x-y;
		System.out.println(z);
	}
	
}
